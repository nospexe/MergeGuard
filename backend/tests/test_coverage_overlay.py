# TestCoverageFileReader — tests the .coverage file reader
# ---------------------------------------------------------------------------

class TestCoverageFileReader:
    """
    Tests for reading actual .coverage files.
    We generate a real .coverage file by running coverage.py
    programmatically on a tiny Python script.
    """

    def _generate_coverage_file(self, tmp_path: Path) -> tuple[Path, Path]:
        """
        Create a tiny Python file, run it under coverage.py, and return
        the paths to (source_file, coverage_file).
        """
        try:
            import coverage as coverage_lib
        except ImportError:
            pytest.skip("coverage package not installed")

        source = tmp_path / "subject.py"
        source.write_text(textwrap.dedent("""\
            def covered():
                x = 1
                return x

            def uncovered():
                y = 2
                return y
        """))

        cov_file = tmp_path / ".coverage"
        cov = coverage_lib.Coverage(data_file=str(cov_file))
        cov.start()
        try:
            # Execute only `covered()`, leaving `uncovered()` untouched
            ns = {}
            exec(compile(source.read_text(), str(source), "exec"), ns)
            ns["covered"]()
        finally:
            cov.stop()
            cov.save()

        return source, cov_file

    def test_reader_returns_line_coverage_data(self, tmp_path):
        """
        The reader should return a dict containing LineCoverageData
        for the measured file.
        """
        source, cov_file = self._generate_coverage_file(tmp_path)

        reader = CoverageFileReader()
        data = reader.read(cov_file)

        # At least one file should be in the data
        assert len(data) > 0

    def test_covered_lines_are_recorded(self, tmp_path):
        """
        Lines executed during the test run must appear in covered_lines.
        """
        source, cov_file = self._generate_coverage_file(tmp_path)

        reader = CoverageFileReader()
        data = reader.read(cov_file)

        # Find our source file in the data (path matching)
        match = next(
            (v for k, v in data.items() if "subject" in k),
            None,
        )
        assert match is not None, "subject.py not found in coverage data"
        assert len(match.covered_lines) > 0

    def test_raises_on_missing_coverage_file(self, tmp_path):
        """
        Attempting to read a non-existent .coverage file must raise
        FileNotFoundError — not return an empty dict silently.
        """
        reader = CoverageFileReader()
        with pytest.raises(FileNotFoundError):
            reader.read(tmp_path / "nonexistent_coverage_file")


