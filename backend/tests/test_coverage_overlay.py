def write_file(directory: Path, rel_path: str, content: str) -> Path:
    full = directory / rel_path
    full.parent.mkdir(parents=True, exist_ok=True)
    full.write_text(textwrap.dedent(content), encoding="utf-8")
    return full


def make_symbol_definition(
    name: str,
    module_path: str,
    file_path: str,
    line_start: int,
    line_end: int,
    sym_type: str = "function",
) -> SymbolDefinition:
    return SymbolDefinition(
        name=name,
        qualified_name=f"{module_path}.{name}",
        symbol_type=sym_type,
        file_path=file_path,
        line_start=line_start,
        line_end=line_end,
    )


def make_file_table(
    module_path: str,
    file_path: str,
    definitions: list[SymbolDefinition],
) -> FileSymbolTable:
    table = FileSymbolTable(file_path=file_path, module_path=module_path)
    table.definitions = definitions
    return table


def make_line_coverage(
    file_path: str,
    covered: list[int],
    missing: list[int],
) -> LineCoverageData:
    total = len(covered) + len(missing)
    pct = len(covered) / total * 100.0 if total > 0 else 0.0
    return LineCoverageData(
        file_path=file_path,
        covered_lines=covered,
        missing_lines=missing,
        total_lines=total,
        coverage_percent=round(pct, 2),
    )


