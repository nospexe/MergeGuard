"""
test_coverage_overlay.py — Unit Tests for the Coverage Overlay Engine
======================================================================

TESTING STRATEGY
----------------
The coverage overlay engine has one tricky dependency: it reads a binary
`.coverage` file created by coverage.py. We cannot create this file
manually in tests without executing real code.

We handle this with two strategies:

  1. MOCK TESTS (majority)
     We mock the `CoverageFileReader.read()` method to return fake
     LineCoverageData objects we control completely. This lets us test
     the annotation logic (the complex part) without any file I/O.

  2. INTEGRATION TESTS
     We use a real .coverage file generated by running a tiny Python
     file through coverage.py programmatically inside the test itself.
     This validates the file reader against a real coverage artifact.

HOW TO RUN
----------
  cd backend
  pytest tests/test_coverage_overlay.py -v
"""

